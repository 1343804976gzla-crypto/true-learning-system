def parse_video_path(path: str) -> dict:
    """
    解析视频文件路径，提取科目、章节、知识点
    
    示例路径：
    /我的资源/.../病理-西综考点大师精讲课-27/考点精讲-病理（1-4章）/病理03章-03血栓形成（53分钟）.mp4
    /我的资源/.../内科-西综考点大师精讲课-27/考点精讲-内科（11-17章）/内科14章-01二尖瓣狭窄（二狭）（69分钟）.mp4
    """
    # 提取科目
    subject = None
    subject_key = None
    for cn, en in SUBJECT_MAP.items():
        if cn in path:
            subject = SUBJECT_NAME[en]
            subject_key = en
            break
    
    if not subject:
        return None
    
    # 提取文件名
    filename = Path(path).name
    
    # 匹配模式：病理03章-03血栓形成（53分钟）.mp4
    # 或者：内科14章-01二尖瓣狭窄（二狭）（69分钟）.mp4
    pattern = r'(?:病理|内科|外科|生化|诊断|生理|人文)(\d+)章[-_](\d+)([^（(]+)'
    match = re.search(pattern, filename)
    
    if not match:
        # 尝试其他模式
        pattern2 = r'(?:病理|内科|外科|生化|诊断|生理|人文)(\d+)章[-_](\d+)(.+)\.mp4'
        match = re.search(pattern2, filename)
    
    if match:
        chapter_num = match.group(1)
        sub_chapter = match.group(2)
        title_raw = match.group(3).strip()
        
        # 清理标题中的时长信息
        title = re.sub(r'[（(]\d+分钟[）)]', '', title_raw).strip()
        title = re.sub(r'\.mp4$', '', title)
        
        # 生成章节ID（下划线格式）
        chapter_id = f"{subject_key}_ch{chapter_num}"
        
        # 生成知识点ID
        concept_id_safe = title.replace('（', '_').replace('）', '_').replace('(', '_').replace(')', '_')
        concept_id_safe = concept_id_safe.replace(' ', '_').replace('、', '_').replace('/', '_')
        concept_id_safe = re.sub(r'[^\w_]', '', concept_id_safe)
        concept_id = f"{chapter_id}_{sub_chapter}_{concept_id_safe[:30]}"
        
        return {
            "subject_key": subject_key,
            "subject_name": subject,
            "chapter_number": chapter_num,
            "sub_chapter": sub_chapter,
            "chapter_id": chapter_id,
            "concept_id": concept_id,
            "title": title,
            "raw_path": path,
            "filename": filename
        }
    
    return None


def init_database():
    """确保数据库和表存在"""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # 创建chapters表
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS chapters (
            id TEXT PRIMARY KEY,
            book TEXT NOT NULL,
            edition TEXT,
            chapter_number TEXT NOT NULL,
            chapter_title TEXT NOT NULL,
            content_summary TEXT,
            concepts JSON,
            first_uploaded DATE,
            last_reviewed DATE
        )
    """)
    
    # 创建concept_mastery表
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS concept_mastery (
            concept_id TEXT PRIMARY KEY,
            chapter_id TEXT NOT NULL,
            name TEXT NOT NULL,
            retention REAL DEFAULT 0.0,
            understanding REAL DEFAULT 0.0,
            application REAL DEFAULT 0.0,
            last_tested DATE,
            next_review DATE,
            FOREIGN KEY (chapter_id) REFERENCES chapters(id)
        )
    """)
    
    # 创建daily_uploads表
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS daily_uploads (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            date DATE NOT NULL,
            raw_content TEXT NOT NULL,
            ai_extracted JSON,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    """)
    
    conn.commit()
    conn.close()
    print("✅ 数据库初始化完成")


def import_courses(video_paths: list):
    """批量导入课程数据"""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # 按章节分组
    chapters = {}
    concepts = []
    
    for path in video_paths:
        data = parse_video_path(path)
        if not data:
            continue
        
        chapter_id = data["chapter_id"]
        
        # 收集章节信息
        if chapter_id not in chapters:
            chapters[chapter_id] = {
                "id": chapter_id,
                "book": data["subject_name"],
                "edition": "贺银成2027",
                "chapter_number": data["chapter_number"],
                "chapter_title": f"第{data['chapter_number']}章",
                "content_summary": f"贺银成西综考点精讲 - {data['subject_name']}第{data['chapter_number']}章",
                "concepts": [],
                "first_uploaded": date.today().isoformat(),
                "last_reviewed": None
            }
        
        # 添加知识点
        concept = {
            "id": data["concept_id"],
            "name": data["title"],
            "sub_chapter": data["sub_chapter"]
        }
        chapters[chapter_id]["concepts"].append(concept)
        
        concepts.append({
            "concept_id": data["concept_id"],
            "chapter_id": chapter_id,
            "name": data["title"],
            "retention": 0.0,
            "understanding": 0.0,
            "application": 0.0,
            "next_review": date.today().isoformat()
        })
    
    # 插入章节数据
    chapter_count = 0
    for chapter_id, chapter in chapters.items():
        try:
            cursor.execute("""
                INSERT OR IGNORE INTO chapters 
                (id, book, edition, chapter_number, chapter_title, content_summary, concepts, first_uploaded, last_reviewed)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                chapter["id"],
                chapter["book"],
                chapter["edition"],
                chapter["chapter_number"],
                chapter["chapter_title"],
                chapter["content_summary"],
                json.dumps(chapter["concepts"], ensure_ascii=False),
                chapter["first_uploaded"],
                chapter["last_reviewed"]
            ))
            if cursor.rowcount > 0:
                chapter_count += 1
        except Exception as e:
            print(f"❌ 插入章节失败 {chapter_id}: {e}")
    
    # 插入知识点数据
    concept_count = 0
    for concept in concepts:
        try:
            cursor.execute("""
                INSERT OR IGNORE INTO concept_mastery 
                (concept_id, chapter_id, name, retention, understanding, application, next_review)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (
                concept["concept_id"],
                concept["chapter_id"],
                concept["name"],
                concept["retention"],
                concept["understanding"],
                concept["application"],
                concept["next_review"]
            ))
            if cursor.rowcount > 0:
                concept_count += 1
        except Exception as e:
            print(f"❌ 插入知识点失败 {concept['concept_id']}: {e}")
    
    conn.commit()
    conn.close()
    
    print(f"✅ 导入完成：{chapter_count} 个章节，{concept_count} 个知识点")
    return chapters, concepts


def print_summary(chapters: dict, concepts: list):
    """打印导入摘要"""
    print("\n" + "="*60)
    print("📚 导入摘要")
    print("="*60)
    
    # 按科目统计
    subject_stats = {}
    for chapter_id, chapter in chapters.items():
        subject = chapter["book"]
        if subject not in subject_stats:
            subject_stats[subject] = {"chapters": 0, "concepts": 0}
        subject_stats[subject]["chapters"] += 1
    
    for concept in concepts:
        # 从chapter_id反查科目
        chapter_id = concept["chapter_id"]
        if chapter_id in chapters:
            subject = chapters[chapter_id]["book"]
            subject_stats[subject]["concepts"] += 1
    
    for subject, stats in sorted(subject_stats.items()):
        print(f"  {subject}: {stats['chapters']} 章, {stats['concepts']} 个知识点")
    
    print(f"\n总计: {len(chapters)} 章, {len(concepts)} 个知识点")
    print("="*60)


